                    Image(systemName: "chevron.right")
                        .font(.caption)
                        .foregroundColor(.mediumGrey)
                }
            }
        } header: {
            Text("Tutorial & Help")
        } footer: {
            Text("Strategy hints provide tips during gameplay based on your hand.")
        }
    }

