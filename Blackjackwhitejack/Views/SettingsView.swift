                HStack {
                    Image(systemName: "lightbulb.fill")
                        .foregroundColor(.warning)
                    Text("Tutorial Hints")
                }
            }
            .tint(.info)

            // Contextual Hints toggle
            Toggle(isOn: Binding(
                get: { contextualHintsEnabled },
                set: { newValue in
                    contextualHintsEnabled = newValue
                    tutorialManager.setContextualHintsEnabled(newValue)
                }
            )) {
