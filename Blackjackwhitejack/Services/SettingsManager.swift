    private static func loadSettings() -> UserSettings {
        guard let data = UserDefaults.standard.data(forKey: "userSettings") else {
            print("â„¹ï¸ No saved settings found - using defaults")
            return .default
        }

        do {
            let decoder = JSONDecoder()
            var settings = try decoder.decode(UserSettings.self, from: data)

            // Validate loaded settings
            settings.validate()

            print("ğŸ“‚ Loaded user settings from UserDefaults")
            return settings

        } catch {
            print("âŒ Failed to decode settings: \(error.localizedDescription)")
            print("   Using default settings")
            return .default
        }
    }

